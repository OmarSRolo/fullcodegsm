     * @return CI_Output
     * @inheritdoc Devuelve en JSON todos los datos del servicio
     */
    public function getData()
    {


        $DARA = new stdClass();
//        $info = $this->service();
        $info = $this->data();


        $servicios = $this->processData($info);

        // Obtiene la cache
        $cacheData = $this->cache->file->get('data_service');

        // Si no tiene cache
        if (!$cacheData) {
            // Salva la cache
            $this->cache->file->save('data_service', $servicios, 1000000);

            // $this->empresa_model->insert_many($empresa);

            // Elimina los registros de ese servicio
            $this->servicio_model->truncate();

            // Inserta los servicios en la BD
            $this->servicio_model->insert_many($servicios);

            // Obtengo todos los servicios desde la base de datos
            $dataFromDB = $this->servicio_model->get_all_data();
            $DARA->data = $dataFromDB;
            return $this->output
                ->set_content_type('application/json')
                ->set_output(json_encode($DARA));

            // Si tiene cache
        } else {

            // Obten todos los datos de la base de datos
            $dataFromDB = $this->servicio_model->get_all_data();

            // Compara las diferencia entre la cache y la base de datos
            // Si hay diferencia elimina los registros y la cache
            // de ese servicio de la base de datos y los inserta otra vez.
            $b = $dataFromDB == $cacheData;
            if ($b == false) {
                $this->cache->file->delete('data_service');
                $this->cache->file->save('data_service', $servicios, 1000000);
                $this->servicio_model->truncate();
                $this->servicio_model->insert_many($servicios);
                $DARA->data = $dataFromDB;
                return $this->output
                    ->set_content_type('application/json')
                    ->set_output(json_encode($DARA));

                // Si no hay diferencia. Devuelve la cache
            } else {
                $DARA->data = $cacheData;
                return $this->output
                    ->set_content_type('application/json')
                    ->set_output(json_encode($DARA));
            }
        }
    }


