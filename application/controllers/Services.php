    /**
     * @param $info
     * @inheritdoc Procesa todos los datos del servicio
     * @return array
     */
    private function processData($info)
    {
        $json_decode = json_decode($info, true);
        $data = $json_decode["SUCCESS"][0]["LIST"];

        $empresa = array();
        $servicios = array();
